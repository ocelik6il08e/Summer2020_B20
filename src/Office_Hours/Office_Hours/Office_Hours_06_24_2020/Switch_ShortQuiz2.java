package Office_Hours.Office_Hours.Office_Hours_06_24_2020;

public class Switch_ShortQuiz2 {

    public static void main(String[] args) {
        String opt = "true";

        switch (opt){
            case "true":
                System.out.println("True");
                break;

            default:
                System.out.println("False");
        }
        System.out.println("Done");
    }
}
