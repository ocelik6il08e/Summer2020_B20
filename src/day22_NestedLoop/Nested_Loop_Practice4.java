public class Nested_Loop_Practice4 {
    public static void main(String[] args) {
        int i =0;
        while(i <= 7){
            for (int j = 0; i>=j ; j++) {
                System.out.print("*");

            }
            System.out.println();
            i++;
        }

    }
}

