package day22_NestedLoop;

public class Nested_Loop3 {
    public static void main(String[] args) {

        int i = 1;
        while (i <= 4) {
            for (int j = 1; j <= 10; j++) {

                System.out.print("*");

            }
            System.out.println();
            i++;

        }


    }
}

