package Office_Hours_07_20_2020.Office_Hours.Office_Hours_06_24_2020;

public class Switch_ShortQuiz {

    public static void main(String[] args) {
        int a = 15;

        switch (a) {

            case 10:
                System.out.println("Monday");
                break;
            case 11:
                System.out.println("Tuesday");
                break;
            case 12:
                System.out.println("Wednesday");
                break;
            default:
                System.out.println("Friday");

                /*
                Boolean, doublean, long, float data types are not accepted for switch statements.
                 */

        }
    }
}
